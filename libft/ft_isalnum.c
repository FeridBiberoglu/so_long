#include <unistd.h>

int	ft_isalnum(char s)
{
	if (s >= 48 && s <= 57)
		return (1);
	else if ((s >= 97 && s <= 122) || (s >= 65 && s <= 90))
		return (1);
	else
		return (0);
}

